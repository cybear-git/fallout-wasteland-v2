CREATE TABLE IF NOT EXISTS characters (
    id INT AUTO_INCREMENT PRIMARY KEY,
    player_id INT UNSIGNED UNIQUE NOT NULL,
    name VARCHAR(50) NOT NULL,
    gender ENUM('male', 'female') DEFAULT 'male',
    level TINYINT UNSIGNED DEFAULT 1,
    xp INT UNSIGNED DEFAULT 0,
    hp SMALLINT UNSIGNED DEFAULT 100,
    max_hp SMALLINT UNSIGNED DEFAULT 100,
    radiation TINYINT UNSIGNED DEFAULT 0,
    strength TINYINT UNSIGNED DEFAULT 5,
    perception TINYINT UNSIGNED DEFAULT 5,
    endurance TINYINT UNSIGNED DEFAULT 5,
    charisma TINYINT UNSIGNED DEFAULT 5,
    intelligence TINYINT UNSIGNED DEFAULT 5,
    agility TINYINT UNSIGNED DEFAULT 5,
    luck TINYINT UNSIGNED DEFAULT 5,
    pos_x INT DEFAULT 0,
    pos_y INT DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT fk_char_player FOREIGN KEY (player_id) REFERENCES players(id) ON DELETE RESTRICT,
    INDEX idx_level (level),
    INDEX idx_coords (pos_x, pos_y)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;